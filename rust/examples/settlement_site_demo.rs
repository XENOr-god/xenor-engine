use std::env;
use std::error::Error;
use std::fmt::Write as _;
use std::fs;
use std::path::{Path, PathBuf};

use xenor_engine_rust::{
    SettlementArtifactDigests, SettlementArtifactSizes, SettlementDemoCatalog,
    SettlementDemoScenarioView, SettlementDeterminismReport, SettlementRunSummary,
    SettlementScenarioExpectation, SettlementSimulationConfig, SettlementStatus,
    SettlementTickDigestView, WorkerAllocation, build_settlement_demo_catalog,
};

fn main() -> Result<(), Box<dyn Error>> {
    let output_path = env::args()
        .nth(1)
        .map(PathBuf::from)
        .unwrap_or_else(default_output_path);
    let catalog = build_settlement_demo_catalog()?;
    let rendered = render_catalog_module(&catalog);

    if let Some(parent) = output_path.parent() {
        fs::create_dir_all(parent)?;
    }
    fs::write(&output_path, rendered)?;

    println!("wrote {}", output_path.display());
    Ok(())
}

fn default_output_path() -> PathBuf {
    Path::new(env!("CARGO_MANIFEST_DIR"))
        .join("../../xenor-web/app/lib/generated/settlement-demo.generated.ts")
}

fn render_catalog_module(catalog: &SettlementDemoCatalog) -> String {
    let mut out = String::new();
    out.push_str("import type { SettlementDemoCatalog } from \"../settlement-demo-contract\";\n\n");
    out.push_str("// Generated by `cargo run --manifest-path rust/Cargo.toml --example settlement_site_demo`.\n");
    out.push_str("// Do not edit by hand.\n\n");
    out.push_str("export const settlementDemoCatalog = {\n");
    push_line(
        &mut out,
        1,
        format!("engineFamily: {},", js_string(&catalog.engine_family)),
    );
    push_line(
        &mut out,
        1,
        format!("scenarioCount: {},", catalog.scenario_count),
    );
    push_line(&mut out, 1, "scenarios: [".into());
    for scenario in &catalog.scenarios {
        render_scenario(&mut out, scenario, 2);
    }
    push_line(&mut out, 1, "],".into());
    out.push_str("} satisfies SettlementDemoCatalog;\n");
    out
}

fn render_scenario(out: &mut String, scenario: &SettlementDemoScenarioView, indent: usize) {
    push_line(out, indent, "{".into());
    push_line(out, indent + 1, format!("id: {},", js_string(&scenario.id)));
    push_line(
        out,
        indent + 1,
        format!("title: {},", js_string(&scenario.title)),
    );
    push_line(
        out,
        indent + 1,
        format!("description: {},", js_string(&scenario.description)),
    );
    push_line(
        out,
        indent + 1,
        format!("seed: {},", js_string(&scenario.seed.to_string())),
    );
    push_line(
        out,
        indent + 1,
        format!("tickCount: {},", scenario.tick_count),
    );
    push_line(out, indent + 1, "config: {".into());
    render_config(out, &scenario.config, indent + 2);
    push_line(out, indent + 1, "},".into());
    push_line(out, indent + 1, "expected: {".into());
    render_expectation(out, &scenario.expected, indent + 2);
    push_line(out, indent + 1, "},".into());
    push_line(out, indent + 1, "runSummary: {".into());
    render_run_summary(out, &scenario.run_summary, indent + 2);
    push_line(out, indent + 1, "},".into());
    push_line(out, indent + 1, "artifactSummary: {".into());
    render_artifact_summary(out, scenario, indent + 2);
    push_line(out, indent + 1, "},".into());
    push_line(out, indent + 1, "artifactDigests: {".into());
    render_artifact_digests(out, &scenario.artifact_digests, indent + 2);
    push_line(out, indent + 1, "},".into());
    push_line(out, indent + 1, "artifactSizes: {".into());
    render_artifact_sizes(out, &scenario.artifact_sizes, indent + 2);
    push_line(out, indent + 1, "},".into());
    push_line(out, indent + 1, "tickDigestView: [".into());
    for tick in &scenario.tick_digest_view {
        render_tick_digest(out, tick, indent + 2);
    }
    push_line(out, indent + 1, "],".into());
    push_line(out, indent + 1, "determinism: {".into());
    render_determinism(out, &scenario.determinism, indent + 2);
    push_line(out, indent + 1, "},".into());
    push_line(out, indent, "},".into());
}

