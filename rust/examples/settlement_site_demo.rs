fn render_allocation(out: &mut String, allocation: WorkerAllocation, indent: usize) {
    push_line(out, indent, format!("farmers: {},", allocation.farmers));
    push_line(out, indent, format!("loggers: {},", allocation.loggers));
}

fn render_string_array(values: &[String]) -> String {
    let rendered = values
        .iter()
        .map(|value| js_string(value))
        .collect::<Vec<_>>()
        .join(", ");
    format!("[{rendered}]")
}

fn render_optional_string(value: Option<&str>) -> String {
    value.map(js_string).unwrap_or_else(|| "null".into())
}

fn render_optional_u64(value: Option<u64>) -> String {
    value
        .map(|value| js_string(&value.to_string()))
        .unwrap_or_else(|| "null".into())
}

fn render_optional_usize(value: Option<usize>) -> String {
    value
        .map(|value| value.to_string())
        .unwrap_or_else(|| "null".into())
}

fn render_optional_tick(value: Option<u64>) -> String {
    value
        .map(|value| value.to_string())
        .unwrap_or_else(|| "null".into())
}

fn js_string(value: &str) -> String {
    format!("{value:?}")
}

fn push_line(out: &mut String, indent: usize, content: String) {
    let _ = writeln!(out, "{}{}", "  ".repeat(indent), content);
}

#[allow(dead_code)]
fn _status_as_str(status: SettlementStatus) -> &'static str {
    status.as_str()
}
